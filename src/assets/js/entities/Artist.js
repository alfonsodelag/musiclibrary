class Artist {
    constructor(artistName, musicalGenre, artistLink){
        this.artistName= artistName;
        this.musicalGenre= musicalGenre;
        this.artistLink= artistLink;
    }
    getArtistName(){
        return this.artistName;
    }
    getMusicalGenre(){
        return this.musicalGenre;
    }
    getArtistLink(){
        return this.artistLink;
    }
}
